// Helper function to get product by ID
export const getProductById = (id: number | string): Product | undefined => {
    const numId = typeof id === 'string' ? parseInt(id, 10) : id;
    return products.find(p => p.id === numId);
};

// Helper function to get product by slug
export const getProductBySlug = (slug: string): Product | undefined => {
    return products.find(p => p.slug === slug);
};

// Helper function to get products by category
export const getProductsByCategory = (category: string): Product[] => {
    if (category === 'All' || !category) return products;
    return products.filter(p => p.category.toLowerCase() === category.toLowerCase());
};

// Helper to get featured products
export const getFeaturedProducts = (): Product[] => {
    return products.filter(p => p.isFeatured);
};

// Helper to get new arrivals
export const getNewArrivals = (): Product[] => {
    return products.filter(p => p.isNewArrival);
};

// Export for Featured Section on home page (first 4 featured products)
export const FEATURED_PRODUCTS_HOME = products
    .filter(p => p.isFeatured && p.tag)
    .slice(0, 4)
    .map(p => ({
        id: String(p.id),
        name: p.name,
        price: `₹${p.price.toLocaleString()}`,
        originalPrice: p.originalPrice ? `₹${p.originalPrice.toLocaleString()}` : undefined,
        category: p.category,
        image: p.images[0],
        videoUrl: p.videoUrl,
        tag: p.tag
    }));

// Search products by keyword
export const searchProducts = (query: string): Product[] => {
    if (!query || query.trim() === '') return products;

    const searchTerm = query.toLowerCase().trim();

    return products.filter(p => {
        const searchableText = [
            p.name,
            p.category,
            p.subcategory,
            p.brand,
            p.description,
            p.material,
            p.tag
        ].filter(Boolean).join(' ').toLowerCase();

        return searchableText.includes(searchTerm);
    });
};
